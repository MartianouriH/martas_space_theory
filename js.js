window.onload = () => {
    document.querySelectorAll('.planet-info').forEach(
        block => block.onclick = (event) => {
            const planet = event.target.dataset.planet

            document.querySelector('.about').innerHTML = additional[planet]
            document.querySelector('.additional').classList.add('show')
            document.querySelector('.close').classList.add('show')
        }
    )

    document.querySelector('.close').onclick = () => {
        document.querySelector('.about').innerHTML = ''
        document.querySelector('.additional').classList.remove('show') 
    }

}









































const random = (max) => {
    return (Math.floor(Math.random() * Math.floor(max)) + 1);
  }
  
  const stars = () => {
    const width = window.innerWidth;
    const height = window.innerHeight;
    let i = 0;
    
    // C O N F I G
    const sizeLimit = 1; // max star size (in pixels)
    const count = width / 5; // lower number === more stars
    const twinkle = false;
    // T H A T I S A L L
    
    while (i < count) {
      const randomSize = random(sizeLimit);
      const opacity = random(100);
      const randomTop = random(height);
      const randomLeft = random(width);
      const randomHue = random(360);
      const glow = `
        0 0 ${ randomSize * 7 }px hsl(${ randomHue }, 50%, 50%)
      `;
      const twinkleTime = (random(10) / 10) + 2;
      
      document.body.innerHTML += `
        <div class="star ${ twinkle === true ? 'twinkle' : '' }"
          style="
            opacity: ${ opacity / 100 };
            height: ${ randomSize }px;
            width: ${ randomSize }px;
            top: ${ randomTop }px;
            left: ${ randomLeft }px;
            animation-duration: ${ twinkleTime }s;
            box-shadow:
              inset 0 0 ${ randomSize / 4 }px hsl(${ randomHue }, 50%, 50%),
              ${
                randomSize === sizeLimit && opacity > 50 ?
                glow : false
              };
          "
        ></div>
      `;
      i++;
    }
  }
  
  stars();